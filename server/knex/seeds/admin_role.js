//----------------------------------------------------------------------------------------------------------------------
// Set us up a default role
//----------------------------------------------------------------------------------------------------------------------

const logger = require('trivial-logging').loggerFor(module);

//----------------------------------------------------------------------------------------------------------------------

exports.seed = async (knex) =>
{
    try
    {
        await knex('role')
            .insert({
                name: 'Admins',
                permissions: JSON.stringify([ '*/*' ])
            });
    }
    catch(error)
    {
        logger.warn('Error running seed:', error);
    } // end try/catch
};

