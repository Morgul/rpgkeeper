// Managers
import dbMan from '../database';

// Models
import { Role } from '../models/role';

// ---------------------------------------------------------------------------------------------------------------------

export async function list() : Promise<unknown>
{
    const db = await dbMan.getDB();
    return (await db('role')
        .select())
        .map(Role.fromDB);
} // end list

