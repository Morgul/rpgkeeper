import $http from 'axios';

//----------------------------------------------------------------------------------------------------------------------

class SystemsResourceAccess
{
    async loadSystems()
    {
        const { data } = await $http.get('/systems');
        return data;
    } // end loadSystems
} // end SystemsResourceAccess

//----------------------------------------------------------------------------------------------------------------------

export default new SystemsResourceAccess();

