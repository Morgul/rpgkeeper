import $http from 'axios';

//----------------------------------------------------------------------------------------------------------------------

class AuthResourceAccess
{
    completeSignIn(idToken)
    {
        return $http.post('/auth/google', { idToken })
            .then(({ data }) => data);
    } // end completeSignIn
} // end AuthResourceAccess

//----------------------------------------------------------------------------------------------------------------------

export default new AuthResourceAccess();

