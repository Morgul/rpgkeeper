<template>
    <!-- eslint-disable-next-line vue/no-v-html -->
    <div class="markdown d-inline-block" v-html="renderedContent"></div>
</template>

<!--------------------------------------------------------------------------------------------------------------------->

<style lang="scss" scoped>
    .markdown {
    }
</style>

<!--------------------------------------------------------------------------------------------------------------------->

<script>
    //------------------------------------------------------------------------------------------------------------------

    import marked from 'marked';

    //------------------------------------------------------------------------------------------------------------------

    export default {
        name: 'MarkdownBlock',
        props: {
            text: {
                type: String,
                required: true
            },
            inline: {
                type: Boolean,
                default: false
            }
        },
        computed: {
            renderedContent()
            {
                let rawMarkup = marked(this.text);

                if(this.inline !== false)
                {
                    rawMarkup = rawMarkup.trim().replace(/^(?:<p>)?(.*?)(?:<\/p>)?$/, '$1');
                } // end if

                return rawMarkup;
            }
        }
    };
</script>

