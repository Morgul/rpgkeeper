//----------------------------------------------------------------------------------------------------------------------
/// SystemManager
///
/// @module
//----------------------------------------------------------------------------------------------------------------------

import _ from 'lodash';

//----------------------------------------------------------------------------------------------------------------------

class SystemManager {
    constructor()
    {
        this.systems = [];
    } // end constructor

    get(id)
    {
        return _.find(this.systems, { id });
    } // end get

    register(id, name, description, router)
    {
        this.systems.push({ id, name, description, router });
        this.systems = _.uniq(this.systems, 'id');
    } // end registerSystem
} // end SystemManager

//----------------------------------------------------------------------------------------------------------------------

export default new SystemManager();

